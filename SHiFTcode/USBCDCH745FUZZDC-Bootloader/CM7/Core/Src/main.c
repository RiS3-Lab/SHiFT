/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

	/* Enable I-Cache */
	//SCB_EnableICache();  // we will only use the instruction cache
	/* Enable D-Cache */
	//SCB_EnableDCache();

  /* USER CODE END 1 */
/* USER CODE BEGIN Boot_Mode_Sequence_0 */
	  int32_t timeout;
/* USER CODE END Boot_Mode_Sequence_0 */

/* USER CODE BEGIN Boot_Mode_Sequence_1 */
  /* Wait until CPU2 boots and enters in stop mode or timeout*/
  timeout = 0xFFFFF;
  while((__HAL_RCC_GET_FLAG(RCC_FLAG_D2CKRDY) != RESET) && (timeout-- > 0));
  if ( timeout < 0 )
  {
    Error_Handler();
  }
/* USER CODE END Boot_Mode_Sequence_1 */
  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

/* Configure the peripherals common clocks */
  PeriphCommonClock_Config();
/* USER CODE BEGIN Boot_Mode_Sequence_2 */
	/* When system initialization is finished, Cortex-M7 will release Cortex-M4 by means of
	HSEM notification */
    Fuzzer_t *pAFLfuzzer = (Fuzzer_t *)AFLfuzzerRegion;
    memset((void* )pAFLfuzzer ,0, sizeof(Fuzzer_t));


	/*HW semaphore Clock enable*/
	__HAL_RCC_HSEM_CLK_ENABLE();
	/*Take HSEM */
	HAL_HSEM_FastTake(HSEM_ID_0);
	/*Release HSEM in order to notify the CPU2(CM4)*/
	HAL_HSEM_Release(HSEM_ID_0,0);
	/* wait until CPU2 wakes up from stop mode */
	timeout = 0xFFFF;
	while((__HAL_RCC_GET_FLAG(RCC_FLAG_D2CKRDY) == RESET) && (timeout-- > 0));
	if ( timeout < 0 )
	{
	  Error_Handler();
	}
/* USER CODE END Boot_Mode_Sequence_2 */

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_RNG_Init();
  MX_CRC_Init();
  MX_FDCAN1_Init();
  MX_USART3_UART_Init();
  /* USER CODE BEGIN 2 */


  // How to calculate the configuration values for CAN
  // http://www.bittiming.can-wiki.info/
  // 40 Mhz, 500 KHz
  // - pre-scaler: 4, seg1: 14, seg2: 5
  // - pre-scaler: 5, seg1: 11, seg2: 4
  // 80 Mhz, 500 KHz
  // - pre-scaler: 10, seg1: 11, seg2: 4
  // CAN connections:
  // FDCAN1: TX-> PD1, RX-> PD0 (CM7)
  // FDCAN2: TX-> PB6, RX-> PB5 (CM4)



  led_init();


  app_main();

  // it should never reach beyond this point since the RTOS kernel is in execution at this point


  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

	  //FuzzingTestWrapper();
	 if (HAL_GPIO_ReadPin(B1_GPIO_Port, B1_Pin))
	 {
	     TxData[0] = ubKeyNumber++;
	     TxData[1] = 0xAD;

	     if(HAL_FDCAN_AddMessageToTxFifoQ(&hfdcan1, &TxHeader, TxData) != HAL_OK)
	     {
	  	   HAL_Delay(250);
	  	   HAL_GPIO_TogglePin(LD1_GPIO_PORT, LD1_GPIO_PIN);
	  	   HAL_Delay(250);
	  	   HAL_GPIO_TogglePin(LD1_GPIO_PORT, LD1_GPIO_PIN);

         }
	     HAL_Delay(20);
	     while(HAL_GPIO_ReadPin(B1_GPIO_Port, B1_Pin));
	 }


    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

